#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	if (size < ft_strlen(dst))
		return (size + ft_strlen(src));
	i = 0;
	while (dst[i] != '\0')
		i++;
	j = 0;
	while ((i + j < size - 1) && src[j])
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (i + ft_strlen(src));
}
