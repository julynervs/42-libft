#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	char	len;
	size_t	i;

	len = ft_strlen(s);
	str = ft_calloc(len + 1, sizeof(char));
	if (!s || !str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	return (str);
}
